     * @return \Illuminate\Http\Response
     */
    public function index()
    {   
        //If search query present 
        if(Input::has('q')){
            $search = Input::get('q');
            $books = Book::where('title', 'LIKE', '%'.$search.'%')
                ->orWhere('author', 'LIKE', '%'.$search.'%')
                ->paginate(15);
        }
        else{
            $books = Book::paginate(15);
        }
        
        return view('books.index', compact('books'));
    }

